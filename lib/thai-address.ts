// Thai Address Data — จังหวัด 77 จังหวัด + รหัสไปรษณีย์ → จังหวัด
// ========================================================================

export interface AddressData {
  house_no: string      // บ้านเลขที่ / ชื่ออาคาร / ชั้น / หมู่บ้าน
  soi: string           // ซอย
  road: string          // ถนน
  subdistrict: string   // ตำบล / แขวง
  district: string      // อำเภอ / เขต
  province: string      // จังหวัด
  postal_code: string   // รหัสไปรษณีย์
}

export const EMPTY_ADDRESS: AddressData = {
  house_no: '', soi: '', road: '',
  subdistrict: '', district: '', province: '', postal_code: '',
}

/** Format structured address to single-line display string */
export function formatAddress(addr: AddressData): string {
  const parts = [
    addr.house_no,
    addr.soi ? `ซ.${addr.soi}` : '',
    addr.road ? `ถ.${addr.road}` : '',
    addr.subdistrict ? (addr.province === 'กรุงเทพมหานคร' ? `แขวง${addr.subdistrict}` : `ต.${addr.subdistrict}`) : '',
    addr.district ? (addr.province === 'กรุงเทพมหานคร' ? `เขต${addr.district}` : `อ.${addr.district}`) : '',
    addr.province ? `จ.${addr.province}` : '',
    addr.postal_code,
  ].filter(Boolean)
  return parts.join(' ')
}

/** Try to parse address — JSON or legacy text string */
export function parseAddress(raw: string | null): AddressData {
  if (!raw) return { ...EMPTY_ADDRESS }
  try {
    const parsed = JSON.parse(raw)
    if (typeof parsed === 'object' && parsed !== null && 'house_no' in parsed) {
      return { ...EMPTY_ADDRESS, ...parsed }
    }
  } catch {
    // Not JSON — treat as legacy plain text → put in house_no
  }
  return { ...EMPTY_ADDRESS, house_no: raw }
}

/** Serialize to JSON string for DB storage */
export function serializeAddress(addr: AddressData): string {
  return JSON.stringify(addr)
}

